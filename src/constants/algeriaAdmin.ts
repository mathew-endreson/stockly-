const normalizeAdminKey = (value: string): string =>
  String(value || '')
    .normalize('NFD')
    .replace(/[\u0300-\u036f]/g, '')
    .replace(/[\u2018\u2019\u201B\u2032]/g, "'")
    .replace(/\s+/g, ' ')
    .trim()
    .toLowerCase();

const WILAYA_INDEX = Object.keys(ALGERIA_ADMIN_HIERARCHY).reduce<Record<string, string>>((acc, wilayaName) => {
  acc[normalizeAdminKey(wilayaName)] = wilayaName;
  return acc;
}, {});

const resolveWilayaName = (wilayaName: string): string | null => {
  const key = normalizeAdminKey(wilayaName);
  return WILAYA_INDEX[key] || null;
};

export const getDairasByWilaya = (wilayaName: string): string[] => {
  const resolved = resolveWilayaName(wilayaName);
  if (!resolved) return [];
  return Object.keys(ALGERIA_ADMIN_HIERARCHY[resolved] || {});
};

export const getCommunesByWilayaAndDaira = (wilayaName: string, dairaName: string): string[] => {
  const resolved = resolveWilayaName(wilayaName);
  if (!resolved) return [];
  return ALGERIA_ADMIN_HIERARCHY[resolved]?.[dairaName] || [];
};
