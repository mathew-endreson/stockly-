type TranslationPrimitive = string | number | boolean | null;
interface TranslationMap {
  [key: string]: TranslationPrimitive | TranslationMap;
}

const isTranslationObject = (value: unknown): value is TranslationMap =>
  typeof value === 'object' && value !== null && !Array.isArray(value);

const mergeTranslations = (base: TranslationMap, override: TranslationMap): TranslationMap => {
  const merged: TranslationMap = { ...base };
  for (const [key, value] of Object.entries(override)) {
    const baseValue = merged[key];
    if (isTranslationObject(baseValue) && isTranslationObject(value)) {
      merged[key] = mergeTranslations(baseValue, value);
      continue;
    }
    merged[key] = value;
  }
  return merged;
};

const mergedEnTranslations = mergeTranslations(enTranslations, translationOverrides.en as TranslationMap);
const mergedArTranslations = mergeTranslations(arTranslations, translationOverrides.ar as TranslationMap);
const mergedFrTranslations = mergeTranslations(frTranslations, translationOverrides.fr as TranslationMap);

// Initialize i18n (guard prevents re-initialization on HMR reloads)
if (!i18n.isInitialized) {
  i18n
    .use(LanguageDetector)
    .use(initReactI18next)
    .init({
      resources: {
        en: { translation: mergedEnTranslations },
        ar: { translation: mergedArTranslations },
        fr: { translation: mergedFrTranslations },
      },
      fallbackLng: 'ar',
      interpolation: {
        escapeValue: false,
      },
      detection: {
        order: ['cookie', 'localStorage'],
        caches: ['cookie', 'localStorage'],
      },
    });
} else {
  // HMR reload: refresh resources so new/changed keys take effect without a full page reload
  i18n.addResourceBundle('en', 'translation', mergedEnTranslations, true, true);
  i18n.addResourceBundle('ar', 'translation', mergedArTranslations, true, true);
  i18n.addResourceBundle('fr', 'translation', mergedFrTranslations, true, true);
}

interface LanguageContextType {
  language: 'en' | 'ar' | 'fr';
  setLanguage: (lang: 'en' | 'ar' | 'fr') => void;
  isRTL: boolean;
}

const LanguageContext = createContext<LanguageContextType | undefined>(undefined);

export const LanguageProvider: React.FC<{ children: ReactNode }> = ({ children }) => {
  const [language, setLanguageState] = useState<'en' | 'ar' | 'fr'>('ar');
  const [isInitialized, setIsInitialized] = useState(false);

  // Initialize language from cookie
  useEffect(() => {
    const savedLanguage =
      (Cookies.get('i18next') as 'en' | 'ar' | 'fr' | undefined) ||
      ((typeof window !== 'undefined'
        ? window.localStorage.getItem('i18next')
        : null) as 'en' | 'ar' | 'fr' | null);
    if (savedLanguage && ['en', 'ar', 'fr'].includes(savedLanguage)) {
      setLanguageState(savedLanguage);
      i18n.changeLanguage(savedLanguage);
    } else {
      setLanguageState('ar');
      i18n.changeLanguage('ar');
    }
    setIsInitialized(true);
  }, []);

  // Update document direction when language changes
  useEffect(() => {
    if (!isInitialized) return;

    const isRTL = language === 'ar';
    document.documentElement.dir = isRTL ? 'rtl' : 'ltr';
    document.documentElement.lang = language;
    
    i18n.changeLanguage(language);
    Cookies.set('i18next', language, { expires: 365 });
    if (typeof window !== 'undefined') {
      window.localStorage.setItem('i18next', language);
    }
  }, [language, isInitialized]);

  const setLanguage = (lang: 'en' | 'ar' | 'fr') => {
    setLanguageState(lang);
  };

  if (!isInitialized) {
    return null;
  }

  return (
    <LanguageContext.Provider
      value={{
        language,
        setLanguage,
        isRTL: language === 'ar',
      }}
    >
      {children}
    </LanguageContext.Provider>
  );
};

export const useLanguage = () => {
  const context = useContext(LanguageContext);
  if (context === undefined) {
    throw new Error('useLanguage must be used within a LanguageProvider');
  }
  return context;
};

export { i18n };

